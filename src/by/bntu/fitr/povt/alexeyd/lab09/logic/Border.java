package by.bntu.fitr.povt.alexeyd.lab09.logic;

public class Border {

    private int day;
    private int month;

    public Border() {
    }

    public Border(int month, int day) {
        this.month = month;
        this.day = day;
    }

    public int getDay() {
        return day;
    }

    public int getMonth() {
        return month;
    }

}
